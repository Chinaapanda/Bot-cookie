SLIDE_Y = 937
SLIDE_HOLD_MS = 400   # ระยะเวลากดค้างตอนสไลด์ (มิลลิวินาที)

# ---------------------------------------------------------------------------
# โซนตรวจจับ (Region Of Interest)
# ค่าเป็นสัดส่วน 0.0-1.0 ของความกว้าง/สูงจอ เพื่อให้ใช้ได้กับหลายความละเอียด
# โซนนี้คือ "พื้นที่ข้างหน้าคุกกี้" ที่อุปสรรคจะวิ่งเข้ามา
# ---------------------------------------------------------------------------
# โซนตรวจอุปสรรคที่ต้อง "กระโดด" (หลุม/สิ่งกีดขวางระดับพื้น) - อยู่ด้านหน้าและล่าง
ROI_JUMP = (0.46, 0.55, 0.70, 0.90)   # (x1, y1, x2, y2) สัดส่วน

# โซนตรวจอุปสรรคที่ต้อง "สไลด์" (สิ่งกีดขวางด้านบน) - อยู่ด้านหน้าและบน
ROI_SLIDE = (0.46, 0.20, 0.70, 0.45)

# ตำแหน่งคุกกี้ (ใช้เทียบสีพื้น/ตรวจว่ายังเล่นอยู่ไหม)
ROI_PLAYER = (0.18, 0.45, 0.32, 0.85)

# ---------------------------------------------------------------------------
# พารามิเตอร์การตรวจจับ
# ---------------------------------------------------------------------------
# วิธีตรวจจับ: "motion" = ดูการเปลี่ยนแปลงของภาพในโซน (ง่าย ไม่ต้องเทรน)
#             "template" = จับคู่รูปอุปสรรคที่เซฟไว้ในโฟลเดอร์ templates/
DETECT_METHOD = "motion"

# motion: ถ้าสัดส่วน pixel ที่เปลี่ยนในโซนเกินค่านี้ ถือว่ามีอุปสรรค
