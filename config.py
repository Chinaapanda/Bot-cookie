# template matching: ค่าความเหมือนขั้นต่ำ (0-1) ถึงจะถือว่าเจอ
TEMPLATE_MATCH_THRESHOLD = 0.78

# กันสั่ง action รัว ๆ : เว้นอย่างน้อยกี่วินาทีระหว่าง action เดียวกัน
